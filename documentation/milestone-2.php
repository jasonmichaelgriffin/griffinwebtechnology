		<h2><strong>Use Cases</strong></h2>

		<h3><strong>"Industry Contact"</strong></h3>

		<p>Mr. Lee already knows me socially but not professionally.  My name pops up when asking around for freelance
			project managers & developers.  He has a list of about 20 potential candidates he is looking at.  None of
			these candidates are aware he is even looking for people to work with.<br/>
			While eating breakfast at his kitchen counter he looks me up on Facebook and visits my site via a link on my
			profile.  He’s using an old 1st gen MacBook Air.  He has looked at about a dozen people already.<br/>
			Pulling up my home page he reads through the services I provide, notes the design and overall tone of the
			site, then navigates to the “about” page via a two link menu at the top of the page.  Skimming through my
			biography he scrutinizes my work history.  He notices there is not a portfolio but as that lines up with my
			described work history it doesn’t alarm him.  Seeing no red flags his curiosity is piqued and he navigates to
			the “contact” page, again via the menu at the top of the page.  He then writes me a direct e-mail using my
			address on the contact page asking to set up an appointment.<br>
		</p>

		<h3><strong>"Potential Client"</strong></h3>

		<p>Having met me at a local coffee enthusiasts gathering Mr. Fresh already has my card.<br/>
			At home after dinner he is discussing with his wife the need for an improved web presence and remembers my
			card.  Not knowing much about me other than my love of coffee and that I do something related to web
			technology he digs out my card and navigates to my site using iPad “Retina” (aka iPad 3).<br/>
			He carefully reads over my services and liking what he sees navigates straight to the “contact” page via the
			two item menu at the top of the page.  Once there he clicks on my e-mail address (or pokes, not sure what you
			call it with a tablet), the iPad automatically opens a new e-mail which he fills out asking to set up an
			appointment<br/>
		</p>

